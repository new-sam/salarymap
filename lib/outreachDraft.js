export async function generateDraft(lead, ownerKey = 'wsj', round = 1) {
  // pikdi_targets 캠페인은 owner=wsj 이지만 전용 피치(경쟁사 미언급·직무 기반)를 쓴다.
  const pitchKey = lead.campaign === 'pikdi_targets' ? 'pikdi' : ownerKey
  const pitch = OWNER_PITCH[pitchKey] || OWNER_PITCH.wsj
  const systemPrompt = (round >= 2 && pitch.followup) ? pitch.followup : pitch.system
  const c = splitName(lead.company_name || ''), n = splitName(lead.contact_name || '')
  const ctx = {
    회사명: c.ko || c.en || lead.company_name,
    담당자: n.ko || n.en || lead.contact_name || null,
    업종: lead.industry || null,
    상세업종: lead.industry_detail || null,
    사업내용: lead.business_desc || null,
  }
  const messages = [
    { role: 'system', content: systemPrompt },
    { role: 'user', content: `이 회사에 보낼 메일을 작성하세요:\n${JSON.stringify(ctx, null, 2)}` },
  ]
  for (let attempt = 0; attempt < 2; attempt++) {
    const res = await openai().chat.completions.create({
      model: 'gpt-4o-mini', temperature: pitch.temperature, max_tokens: 2000,
      response_format: { type: 'json_object' }, messages,
    })
    try {
      const parsed = JSON.parse(res.choices[0].message.content)
      let subject = (parsed.subject || '').trim()
      const suffix = ownerKey === 'younghun' ? '베트남 인재 매칭 제안' : '베트남 인재 채용 제안'
      if (subject && !subject.includes(suffix)) {
        subject = subject.replace(/[\s·,]*제안\s*$/, '').trim()  // 모델이 접미어를 '제안'으로 줄여쓴 꼬리 제거 → 중복 방지
        subject = `${subject} ${suffix}`.trim()
      }
      return { subject, body: (parsed.body || '').trim() }
    } catch { /* 드물게 모델이 깨진 JSON 을 냄 → 1회 재시도 */ }
  }
  throw new Error('초안 생성 실패 (JSON 파싱 오류)')
}
