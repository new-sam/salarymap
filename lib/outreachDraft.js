function splitName(s) {
  s = (s || '').trim()
  if (!s) return { en: '', ko: '' }
  if (s.endsWith(')')) {
    let d = 0
    for (let i = s.length - 1; i >= 0; i--) {
      if (s[i] === ')') d++
      else if (s[i] === '(') { d--; if (d === 0) {
        const b = s.slice(0, i).trim(), inn = s.slice(i + 1, -1).trim()
        return /[가-힣]/.test(inn) ? { en: b, ko: inn } : { en: b || inn, ko: '' }
      } }
    }
  }
  return /[가-힣]/.test(s) ? { en: '', ko: s } : { en: s, ko: '' }
}

